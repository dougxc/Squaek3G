package com.sun.squawk.compiler.cgen;

import java.io.PrintStream;
import com.sun.squawk.compiler.*;

/**
 * Class representing an instruction to pop all the data from the runtime stack.
 *
 * @author   Nik Shaylor
 */
public class PopAllOp extends Instruction {

    /**
     * Constructor.
     */
    public PopAllOp() {
        super(VOID);
    }

    /**
     * Call the approperate function of an CodeEmitter.
     *
     * @param emitter the CodeEmitter
     */
    public void emit(CodeEmitter emitter) {
        emitter.emitPopAllOp(this);
    }

    /**
     * Print the IR.
     *
     * @param out the output stream
     */
    public void print(PrintStream out) {
        super.print(out);
        out.print(" PopAllOp ");
    }
}